"use client";

import Image from "next/image";
import { useState, useEffect } from "react";
import { useRouter } from "next/navigation";
import { useSearchParams } from "next/navigation";
import { apiFetch, API_URL } from "./utils/api";

export default function Page() {
  const router = useRouter();
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const [loading, setLoading] = useState(false);
  const searchParams = useSearchParams();

  const handleGoogleLogin = () => {
    window.location.href = `${API_URL}/api/auth/google`;
  };

  const handleLogin = async (e: React.FormEvent) => {
      e.preventDefault();
      setError("");
      setLoading(true);
  
      try {
        const data = await apiFetch("/api/auth/login", {
          method: "POST",
          body: JSON.stringify({ email, password }),
        });
  
        localStorage.setItem("token", data.token);
        localStorage.setItem("user", JSON.stringify(data.user));
  
        if (data.user.role.name_en === "HR Management") {
          router.push("/HRManagement/Profile");
        } else if (data.user.role.name_en === "Employee") {
          router.push("/Employees/Profile");
        }
      } catch (err: unknown) {
        if (err instanceof Error) {
          setError(err.message);
        } else {
          setError("An unexpected error occurred");
        }
      } finally {
        setLoading(false);
      }
  };

  useEffect(() => {
    const errorMsg = searchParams.get("error");
    if (errorMsg === "unauthorized_email") {
      setError("อีเมลนี้ไม่มีอยู่ในระบบ (This email is not registered)");
    } else if (errorMsg) {
      setError("Login failed: " + errorMsg);
    }
  }, [searchParams]);

    return (
      <div className="min-h-screen flex items-center justify-between font-montserrat bg-white">
        <div className="flex flex-col items-left justify-center h-screen ml-[3%] mr-[3%]">
          <Image src="/images/LOGO Employee Hub-Photoroom 1.png" alt="Logo" width={704.29} height={606} />
        </div>

        <div className="bg-[#0D274E] h-screen w-[60%] flex flex-col items-right rounded-l-[100px]">
          <div className="flex flex-col items-center justify-center h-screen mr-[3%] ml-[3%] gap-[3em]"> 
            <h1 className="text-white text-6xl font-bold mb-4">Sign in to continue</h1>
            
            <form id="login-form" onSubmit={handleLogin} className="min-w-3/4 mt-6 flex flex-col gap-6">
              {error && <p className="text-red-500 bg-white p-2 rounded text-center font-bold">{error}</p>}
              
              <div className="w-full">
                <label htmlFor="email" className="text-xl font-Kanit text-[#ffffff]">E-mail</label>
                <input type="email" id="email" value={email} onChange={(e) => setEmail(e.target.value)} placeholder="Enter your registered email address" required className="mt-1 w-full rounded-md border px-4 py-3 placeholder-[#D1D1D1] focus:outline-none text-black bg-white text-xl" />
              </div>

              <div className="w-full">
                <label htmlFor="password" className="text-xl font-Kanit text-[#ffffff] block mb-1">Password</label>
                <div className="relative">
                  <input type="password" id="password" value={password} onChange={(e) => setPassword(e.target.value)} placeholder="Enter your password" required className="mt-1 w-full rounded-md border px-4 py-3 placeholder-[#D1D1D1] focus:outline-none text-black bg-white text-xl" />
                </div>
              </div>
              <div className="w-full flex justify-end">
                <p className="text-white underline text-xs cursor-pointer">Forgot your password?</p>
              </div>
            </form>

            <div className="flex flex-col items-center gap-4 w-full">
              <button
                type="submit"
                form="login-form"
                disabled={loading}
                className="cursor-pointer w-fit px-16 bg-[#D87031] text-white py-3 rounded-full font-bold hover:bg-[#81441e] transition-all duration-300 hover:scale-105 text-3xl disabled:opacity-80 disabled:hover:scale-90"
              >
                {loading ? "Signing in..." : "Sign in"}
              </button>
              
              <div className="text-white text-lg">OR</div>

              <button
                type="button"
                onClick={handleGoogleLogin}
                className="flex items-center gap-3 bg-[#D87031] text-[#ffffff] px-8 py-3 rounded-full font-bold text-xl hover:bg-[#81441e] transition-all duration-300 hover:scale-105 disabled:opacity-80 disabled:hover:scale-50"
              >
                <svg className="w-6 h-6" viewBox="0 0 24 24">
                    <path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" fill="#4285F4" />
                    <path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853" />
                    <path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05" />
                    <path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335" />
                </svg>
                Sign in with Google
              </button>
            </div>
          </div>
        </div>
      </div>
    );
}